    private void b_ida1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_b_ida1ActionPerformed

        try {

            PreparedStatement ps;
            Conectar conle= new Conectar();

            String Origen=opc_Origen.getSelectedItem().toString();
            String Destino=opc_Destino.getSelectedItem().toString();

            int Adu_can=Integer.parseInt(opc_Cantidad_S.getSelectedItem().toString());
            int Nin_can=Integer.parseInt(opc_Can_nino_S.getSelectedItem().toString());

            Date fechatxt = null;

            try {

                java.util.Date fechaIni=Calendario_S.getDate();
                //SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
                fechatxt = new java.sql.Date(fechaIni.getTime());

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Eroror en fecha dia/mes/a単o");
                Logger.getLogger(vuelo.class.getName()).log(Level.SEVERE, null, e);
            }

            ps=conle.getConex().prepareStatement("insert into Reserva_Solo(Origen_solo,Destino_solo,fecha_vuelo_solo,Adulto_Cantidad_solo,Nino_Catidad_solo) values(?,?,?,?,?)");

            ps.setString(1, Origen);
            ps.setString(2, Destino);
            ps.setDate(3,fechatxt);
            ps.setInt(4, Adu_can);
            ps.setInt(5, Nin_can);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(null, "registro guardado");

        } catch (SQLException ex) {
            Logger.getLogger(vuelo.class.getName()).log(Level.SEVERE,null, ex);
            JOptionPane.showMessageDialog(null, "error en el ingreso");
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(vuelo.class.getName()).log(Level.SEVERE, null, ex);
        }

        // Seleccon p1=new Seleccion();
       
    }//GEN-LAST:event_b_ida1ActionPerformed

